#include <unistd.h>

void	print_bits(unsigned char octet)
{
	int				i;
	unsigned char	bit;

	i = 8;
	while (i--)
	{
		bit = (octet >> i & 1) + '0';
		write(1, &bit, 1);
	}
}

int main(void) 
{
    unsigned char octet = 'a';

	print_bits(octet);
	write(1, "\n", 1);
    return (0);
}

